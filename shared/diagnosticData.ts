// SCORING ENGINE
// ============================================================

export type RiskCategory = "low" | "moderate" | "high" | "critical";

export type ScoringOutput = {
  totalScore: number;
  riskCategory: RiskCategory;
  hasCriticalEvent: boolean;
  criticalEvents: string[];
  significantRisks: string[];
  activeRiskBlocks: RiskBlock[];
  mainConclusion: string;
};

export function calculateScore(answers: Record<string, string>): ScoringOutput {
  let totalScore = 0;
  const criticalEvents: string[] = [];
  const significantRisks: string[] = [];
  const activeRiskTags = new Set<string>();

  for (const question of QUESTIONS) {
    const answerId = answers[question.id];
    if (!answerId) continue;

    const option = question.options.find((o) => o.id === answerId);
    if (!option) continue;

    totalScore += option.score;

    if (option.isCriticalEvent && option.riskTag) {
      criticalEvents.push(option.riskTag);
      activeRiskTags.add(option.riskTag);
    }
    if (option.isSignificantRisk && option.riskTag) {
      significantRisks.push(option.riskTag);
      activeRiskTags.add(option.riskTag);
    }
    if (option.riskTag && option.score > 0) {
      activeRiskTags.add(option.riskTag);
    }
  }

  // Determine base category from score
  let riskCategory: RiskCategory;
  if (totalScore <= 6) riskCategory = "low";
  else if (totalScore <= 15) riskCategory = "moderate";
  else if (totalScore <= 24) riskCategory = "high";
  else riskCategory = "critical";

  // Mandatory escalation: if any critical event — minimum "high"
  const hasCriticalEvent = criticalEvents.length > 0;
  if (hasCriticalEvent && (riskCategory === "low" || riskCategory === "moderate")) {
    riskCategory = "high";
  }

  // Build active risk blocks (deduplicated, sorted by severity)
  const severityOrder = { critical: 0, high: 1, moderate: 2 };
  const activeRiskBlocks = Array.from(activeRiskTags)
    .map((tag) => RISK_BLOCKS[tag])
    .filter(Boolean)
    .sort((a, b) => severityOrder[a.severity] - severityOrder[b.severity])
    .slice(0, 5); // max 5 risk blocks

  const mainConclusion = buildMainConclusion(riskCategory, activeRiskBlocks);

  return {
    totalScore,
    riskCategory,
    hasCriticalEvent,
    criticalEvents,
    significantRisks,
    activeRiskBlocks,
    mainConclusion,
  };
}

